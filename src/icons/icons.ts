/**
 * Convert SVG to data URI
 */
function svgToDataUri(svg: string): string {
  const encoded = encodeURIComponent(svg)
    .replace(/'/g, "%27")
    .replace(/"/g, "%22")
  return `data:image/svg+xml,${encoded}`
}

/**
 * Get icons for a specific icon name
 * Returns Icon objects with data URIs
 */
export function getIcons(iconName: string): Icon[] | undefined {
  const svg = ICON_REGISTRY[iconName]
  if (!svg) return undefined

  return [
    {
      url: svgToDataUri(svg),
      mediaType: "image/svg+xml",
    },
  ]
}

/**
 * Get icons for a toolset by ID
 */
export function getToolsetIcons(toolsetId: string): Icon[] | undefined {
  const iconName = TOOLSET_ICONS[toolsetId]
  if (!iconName) return undefined
  return getIcons(iconName)
}

/**
 * Check if an icon exists
 */
export function hasIcon(iconName: string): boolean {
  return iconName in ICON_REGISTRY
}

/**
 * Register a custom icon
 */
export function registerIcon(name: string, svg: string): void {
  ICON_REGISTRY[name] = svg
}

/**
 * Get all available icon names
 */
export function getAvailableIcons(): string[] {
  return Object.keys(ICON_REGISTRY)
}

